#include "libft.h"
#include "fillit.h"

int		ft_oknbtetra(char *s)
{
	int		n;

	n = 1;
	while (*(s + 1))
	{
		if (*s == '\n' && *(s + 1) == '\n')
			n++;
		if (n > 26)
			return (0);
		s++;
	}
	return (1);
}
