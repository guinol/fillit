#include "libft.h"
#include "fillit.h"

void	ft_newpos(char **solution, int *x, int *y)
{
	int		z;

	z = ft_strlen(solution[0]);
	z--;
	if (*y < z)
	{
		++(*y);
	}
	else
	{
		++(*x);
		*y = 0;
	}
}
